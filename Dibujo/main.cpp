void reshape ( int width , int height )
{
	if (height==0) 
		height=1;

	glViewport(0, 0, width,height);

	glMatrixMode(GL_PROJECTION); 
	glLoadIdentity();

	// Ortogonal
	glOrtho(0, 200, 200, 0, -2, 2);

	glMatrixMode(GL_MODELVIEW); 
	glLoadIdentity();
}

